package com.eomcs.basic.ex05;

//# 산술 연산자 : +, -, *, /, %
//
public class Exam1_1 {
  public static void main(String[] args) {
    System.out.println(100 + 27);
    System.out.println(100 - 27);
    System.out.println(100 * 27);
    System.out.println(100 / 27);
    System.out.println(100 % 27); // 나눈 나머지 
  }
}